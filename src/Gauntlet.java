
public class Gauntlet {
public static void main(String[] args) {
	for (int i = 0; i <= 100; i++) {
		System.out.println(i);
		
		
	}
	for (int i = 100; i >= 0; i--) {
		System.out.println(i);
		
	}
	for (int i = 0; i <= 100; i+=2) {
		System.out.println(i);
		
	}
	for (int i = 0; i <= 99; i+=3) {
		System.out.println(i);
		
	}
for (int i = 1; i <= 500; i++) {
System.out.println(i);	
}	
		
	
}
}
